const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const contactSchema = new Schema({
    recipient: {
        type: String,
        required: true
    },
    prefix: {
        type: String,
        required: true
    },
    firstName: {
        type: String,
        required: true
    },
    middleName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    suffix: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    phone: {
        type: String,
        required: true
    },
    street: {
        type: String,
        required: true
    },
    city: {
        type: String,
        required: true
    },
    state: {
        type: String,
        required: true
    },
    country: {
        type: String,
        required: true
    },
    postalCode: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    }
});


module.exports = mongoose.model("Contact", contactSchema);